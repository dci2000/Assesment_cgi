package com.example.assesment.circle;


public class Point {
    private final int x_coordinates;
    private final int y_coordinates;

    public Point(int x, int y){
        this.x_coordinates = x;
        this.y_coordinates = y;
    }

    public int getX_coordinates() {
        return x_coordinates;
    }


    public int getY_coordinates() {
        return y_coordinates;
    }

}
